            <br>
            <h5><f2><text><strong> I. GENERAL : </strong></text></f2></h5>
            <br>
            <p><f2><text><strong>1.</strong></text> Kindly go through the guidelines given below.</f2></p>
            <br>
            <p><f2><text><strong>2.</strong></text> Ensure that the diary is carried to school by your ward every day. Please check the diary every day for entries from the school and also use the diary for communicating with the school staff.</f2></p>
            <p><f2><text><strong>3.</strong></text> Ensure strict adherence to the prescribed uniforms, cleanliness and personal hygiene of your ward.</f2></p>
            <br>
            <p><f2><text><strong>4.</strong></text> See that all required text / notebooks and stationery are carried each day to the school according to the time table.</f2></p>
            <br>
            <p><f2><text><strong>5.</strong></text> Check if homework and other assignments are completed.</f2></p>
            <br>
            <p><f2><text><strong>6.</strong></text> In case your ward is lagging behind in any subject, immediate steps should be taken in consultation with class teacher and subject teachers to make good the deficiency.</f2></p>
            <br>
            <p><f2><text><strong>7.</strong></text> You are requested to refrain from making critical comments on the teacher’s observations in the diary because it is not in good taste.</f2></p>
            <br>
            <p><f2><text><strong>8.</strong></text> You are welcome to communicate in person your constructive suggestions for improving the performance of the child. In addition, you can also give suggestions to Chairman, NCS (VZG) on email ID chairmanncsv@gmail.com.</f2></p>
            <br>
            <p><f2><text><strong>9.</strong></text> Furnish to the school the latest office and residential address and telephone / mobile numbers by making an entry in the diary. These are vital and in the interest of the safety of the students. The mobile number will also assist in communicating through ERP system.</f2></p>
            <br>
            <p><f2><text><strong>10.</strong></text> Pocket money to your wards is to be restricted, and should only be sufficient to cover minor requirements. No money is to be given for events organized by the school unless asked for by the school.</f2></p>
            <br>
            <p><f2><text><strong>11.</strong></text>Expensive articles like video games, cameras, cell phones, calculators, jewellery are not to be brought to school. In case of violation of this rule the articles will be confiscated.</f2></p>
            <br>
            <p><f2><text><strong>12.</strong></text> The school bears no responsibility for articles lost in school.</f2></p>
            <br>
            <p><f2><text><strong>13.</strong></text> Transportation facilities will not be provided by the School.</f2></p>
            <br>
            <p><f2><text><strong>14.</strong></text> Distribution of gifts to children and teachers on birthdays is strictly prohibited.</f2></p>
            <br>
            <p><f2><text><strong>15.</strong></text> Progress reports are to be collected on time.</f2></p>
            <br>
            <p><f2><text><strong>16.</strong></text> Ensure attendance at all PTI’s for effective rapport with the teachers.</f2></p>
            <br>
            <p><f2><text><strong>17.</strong></text> All parents automatically become members of the PTA from the day the child is admitted to the school. They cease to be members when their wards are no more on roll. There is no entry fee for membership. A subscription of Rs.150/- per annum per parent has to be paid towards the PTA fund. Executive Committee may also decide to make adhoc collection to enhance the fund.</f2></p>
            <br>
